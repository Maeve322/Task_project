def capitalize_string():
    """Функция которая делает заглавными все
       буквы каждого слова в строке """
    string = input()
    return print(string.upper())

def capital_letter():
    """Функция которая делает заглавными первые
    буквы каждого слова в строке """
    string = input()
    return print(string.title())